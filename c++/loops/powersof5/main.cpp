#include <iostream>

using namespace std;

int main()
{
    // loop from 5 keep multiplying by 5 until less than 500000.
    for(int i = 5; i < 500000; i = 5 * i)

    // print the current power of 5 followed by space.
    cout << i << " ";

    // print a newline after the loop finishes.
    cout << endl;

    return 0;


}